"""Руководство Китежградского университета Чародейства
и Волшебства считает, что аудиторный фонд используется нерационально –
для проведения занятий выделены большие аудитории, но многие студенты
прогуливают занятия, в результате в аудиториях остается много свободных
мест. Чтобы убедиться в этом, было решено по каждой группе на каждой паре
отмечать количество присутствующих студентов. Тут выяснилась еще одна
проблема – некоторые преподаватели недобросовестно отнеслись к этому
поручению, и отмечали иногда явно нереальные значения – то 0 студентов, то в
два раза больше, чем вообще есть в группе.
Ваша задача – по имеющимся записям о посещаемости одной группы
определить, какая доля всех записей является некорректной.
Некорректными мы будем считать данные, которые сильно отличаются от
остальных (в статистике это называется выбросами). Более точно алгоритм
выглядит так. Пусть у нас есть набор данных, состоящий из N значений.
Вначале найдём квартили для этого набора (Q1, Q2, Q3, см. справку ниже). Затем
определим разброс квартилей IQR = Q3 - Q1. Выбросами (некорректными
данными) будем считать все значения, которые меньше, чем Q1-1.5*IQR, а также
все значения, которые больше, чем Q3+1.5*IQR."""
# Функция для нахождения медианы отсортированного массива
def find_median(arr):
    n = len(arr)
    if n % 2 != 0:  # Если количество элементов нечетное
        return arr[n // 2]  # Берем средний элемент
    else:
        return (arr[n // 2 - 1] + arr[n // 2]) / 2  # Среднее двух центральных

# Считаем количество данных
n = int(input())
arr = []
# Заполняем массив данными о посещаемости
for i in range(n):
    arr.append(int(input()))

# Сортируем данные
sorted_arr = sorted(arr)
# Находим медиану Q2
q2 = find_median(sorted_arr)

# Делим данные на нижнюю и верхнюю половины
if n % 2 != 0:  # Если нечетное количество
    lower_half = sorted_arr[:n // 2]      # Все до середины
    upper_half = sorted_arr[n // 2 + 1:]  # Все после середины
else:
    lower_half = sorted_arr[:n // 2]
    upper_half = sorted_arr[n // 2:]

# Находим Q1 (медиана нижней половины) и Q3 (медиана верхней половины)
q1 = find_median(lower_half)
q3 = find_median(upper_half)

# Вычисляем межквартильный разброс (IQR)
iqr = q3 - q1

# Вычисляем границы для выбросов:
low = q1 - 1.5 * iqr   # Нижняя граница
high = q3 + 1.5 * iqr  # Верхняя граница

# Считаем количество выбросов
count = 0
for data in arr:  # Проверяем каждый исходный элемент
    if data < low or data > high:  # Если выходит за границы
        count += 1

print(count)